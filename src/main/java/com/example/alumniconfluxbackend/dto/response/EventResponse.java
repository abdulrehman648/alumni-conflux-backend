package com.example.alumniconfluxbackend.dto.response;

import com.example.alumniconfluxbackend.util.EventStatus;
import com.example.alumniconfluxbackend.util.TargetAudience;

public class EventResponse {
    private Integer id;
    private String title;
    private String description;
    private String eventDate;
    private String location;
    private EventStatus status;
    private TargetAudience targetAudience;
    private String creatorName;
    private Integer creatorUserId;
    private String createdAt;

    public Integer getCreatorUserId() {
        return creatorUserId;
    }

    public void setCreatorUserId(Integer creatorUserId) {
        this.creatorUserId = creatorUserId;
    }
    private long attendeeCount;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getEventDate() {
        return eventDate;
    }

    public void setEventDate(String eventDate) {
        this.eventDate = eventDate;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public EventStatus getStatus() {
        return status;
    }

    public void setStatus(EventStatus status) {
        this.status = status;
    }

    public TargetAudience getTargetAudience() {
        return targetAudience;
    }

    public void setTargetAudience(TargetAudience targetAudience) {
        this.targetAudience = targetAudience;
    }

    public String getCreatorName() {
        return creatorName;
    }

    public void setCreatorName(String creatorName) {
        this.creatorName = creatorName;
    }

    public String getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }

    public long getAttendeeCount() {
        return attendeeCount;
    }

    public void setAttendeeCount(long attendeeCount) {
        this.attendeeCount = attendeeCount;
    }
}
